import { z } from 'zod';

// ─── Interview Tool ─────────────────────────────────────────────
export const interviewInputSchema = z.object({
  action: z.enum(['start', 'respond', 'score', 'complete']),
  topic: z.string().optional(),
  sessionId: z.string().optional(),
  response: z.string().optional(),
  cwd: z.string().optional(),
  generatedQuestion: z.string().optional()
    .describe('Question generated by the caller LLM (passthrough mode)'),
  ambiguityScore: z.object({
    goalClarity: z.number().min(0).max(1),
    constraintClarity: z.number().min(0).max(1),
    successCriteria: z.number().min(0).max(1),
    priorityClarity: z.number().min(0).max(1),
    contextClarity: z.number().min(0).max(1).optional(),
    contradictions: z.array(z.string()).optional(),
  }).optional().describe('Ambiguity scores computed by the caller LLM (passthrough mode)'),
});

export type InterviewInput = z.infer<typeof interviewInputSchema>;

// ─── Seed Tool ──────────────────────────────────────────────────
export const seedInputSchema = z.object({
  sessionId: z.string(),
  force: z.boolean().optional().default(false),
  seed: z.object({
    goal: z.string(),
    constraints: z.array(z.string()),
    acceptanceCriteria: z.array(z.string()),
    ontologySchema: z.object({ entities: z.array(z.any()), relations: z.array(z.any()) }),
    gestaltAnalysis: z.array(z.any()),
  }).optional().describe('Externally generated seed (passthrough mode)'),
});

export type SeedInput = z.infer<typeof seedInputSchema>;

// ─── Execute Tool ───────────────────────────────────────────────
export const executeInputSchema = z.object({
  action: z.enum(['start', 'plan_step', 'plan_complete', 'status']),
  seed: z.object({
    version: z.string(),
    goal: z.string(),
    constraints: z.array(z.string()),
    acceptanceCriteria: z.array(z.string()),
    ontologySchema: z.object({ entities: z.array(z.any()), relations: z.array(z.any()) }),
    gestaltAnalysis: z.array(z.any()),
    metadata: z.object({
      seedId: z.string(),
      interviewSessionId: z.string(),
      ambiguityScore: z.number(),
      generatedAt: z.string(),
    }),
  }).optional().describe('Seed specification (required for start)'),
  sessionId: z.string().optional().describe('Execute session ID (required for plan_step/plan_complete/status)'),
  stepResult: z.object({
    principle: z.enum(['figure_ground', 'closure', 'proximity', 'continuity']),
    classifiedACs: z.array(z.object({
      acIndex: z.number(),
      acText: z.string(),
      classification: z.enum(['figure', 'ground']),
      priority: z.enum(['critical', 'high', 'medium', 'low']),
      reasoning: z.string(),
    })).optional(),
    atomicTasks: z.array(z.object({
      taskId: z.string(),
      title: z.string(),
      description: z.string(),
      sourceAC: z.array(z.number()),
      isImplicit: z.boolean(),
      estimatedComplexity: z.enum(['low', 'medium', 'high']),
      dependsOn: z.array(z.string()),
    })).optional(),
    taskGroups: z.array(z.object({
      groupId: z.string(),
      name: z.string(),
      domain: z.string(),
      taskIds: z.array(z.string()),
      reasoning: z.string(),
    })).optional(),
    dagValidation: z.object({
      isValid: z.boolean(),
      hasCycles: z.boolean(),
      cycleDetails: z.array(z.string()).optional(),
      hasConflicts: z.boolean(),
      conflictDetails: z.array(z.string()).optional(),
      topologicalOrder: z.array(z.string()),
      criticalPath: z.array(z.string()),
    }).optional(),
  }).optional().describe('Planning step result (required for plan_step)'),
});

export type ExecuteInput = z.infer<typeof executeInputSchema>;

// ─── Status Tool ────────────────────────────────────────────────
export const statusInputSchema = z.object({
  sessionId: z.string().optional(),
});

export type StatusInput = z.infer<typeof statusInputSchema>;
